// Recursively widen all string literal types to `string` so the ES dictionary
// can provide different values while sharing the same structural type.
type DeepStringify<T> = T extends readonly string[]
  ? string[]
  : T extends string
    ? string
    : T extends readonly (infer U)[]
      ? DeepStringify<U>[]
      : T extends object
        ? { [K in keyof T]: DeepStringify<T[K]> }
        : T;

export type TranslationKeys = DeepStringify<typeof en>;
export default en as unknown as TranslationKeys;
